    public void LeerArch() throws IOException {
        //le dices a tu tabla que muestre lo que hay en el contenedor
       
        try {
            BufferedReader leer = new BufferedReader(new FileReader("agenda.txt"));
          
            String renglon = "";
            //String auxiliar = "";
            while ((renglon = leer.readLine()) != null) {//busca datos renglon por renglon hasta  que se acaben
                //auxiliar += renglon + "\n";//guardo los datos que salen en auxiliar
                String[] datos = renglon.split(" _ ");//se sepaa en arreglos 
                String co = datos[0];
                String nom = datos[1];
                String correo = datos[2];
                String tel = datos[3];
                String edad = datos[4];

                C_Tabla.addRow(new Object[]{co, nom, correo, tel, edad});
                Tabla.setModel(C_Tabla);
            }
            leer.close();
            //JOptionPane.showMessageDialog(null,auxiliar); //nuestro los datos encontrados üëÄ

        } catch (FileNotFoundException ex) {

        }
    }

